IF (@BIPAGEM_INI IS NULL)   
BEGIN

    SELECT B.* 
    FROM #TEMP_TABELA_FINAL    B  
    ORDER BY B.EMPRESA, B.NOME_TRANSPORTADORA, B.DATA_EMBARQUE  
    
END ELSE
BEGIN

    SELECT B.*                 
    FROM #TEMP_TABELA_FINAL    B
    WHERE B.DATA_BIPAGEM BETWEEN @BIPAGEM_INI AND @BIPAGEM_FIM 
    ORDER BY B.EMPRESA, B.NOME_TRANSPORTADORA, B.DATA_EMBARQUE  
    
END                                                