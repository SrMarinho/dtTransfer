IF OBJECT_ID ('TEMPDB..#TEMP_TABELA_FINAL') IS NOT NULL
 DROP TABLE #TEMP_TABELA_FINAL

SELECT 
  A.FRETE_PAGAR_TRANSPORTADORA
, A.EMPRESA
, CONCAT(B.ENTIDADE, '-' , B.NOME_FANTASIA) AS NOME_EMPRESA
, A.TRANSPORTADORA
, C.RAZAO_SOCIAL AS NOME_TRANSPORTADORA
, A.NOTA_DESPACHO
, A.NUM_ROMANEIO
, A.DATA_EMBARQUE
, A.NF_FATURAMENTO          
, A.NF_NUMERO
, A.NF_SERIE
, A.ID_NOTA
, A.ENTIDADE_CLIENTE
, CONCAT(D.ENTIDADE, '-', D.NOME) AS NOME_CLIENTE
, A.EMISSAO_NF AS EMISSAO_NF
, F.DATA_HORA AS DATA_BIPAGEM
, A.TOTAL_NF
, A.TOTAL_PRODUTOS
, A.VOLUMES_NF
, A.ROTA
, A.FRETE_CONTRATO  
, CONCAT('Cód.', FRETE_CONTRATO, ' - ', A.PERCENTUAL_PAGAMENTO , '% | Base de cáculo: ', CASE WHEN A.TIPO_CALCULO = 1 THEN 'Valor de NF' WHEN A.TIPO_CALCULO = 2 THEN 'Valor de mercadoria' WHEN A.TIPO_CALCULO = 3 THEN 'Aéreo-AWB' END ) AS DESCRICAO_CONTRATO
, A.PERCENTUAL_PAGAMENTO
, A.TIPO_CALCULO
, A.VALOR_FRETE
, A.ID_AUTORIZACAO_PAGAMENTO  
, 1 AS CONT
, A.STATUS
    INTO #TEMP_TABELA_FINAL

FROM #TEMP_FRETES_LOCALIZADOS        A
JOIN EMPRESAS_USUARIAS                B WITH(NOLOCK) ON B.ENTIDADE = A.EMPRESA
JOIN VW_TRANSPORTADORAS                C WITH(NOLOCK) ON C.ENTIDADE = A.TRANSPORTADORA
LEFT JOIN ENTIDADES                        D WITH(NOLOCK) ON D.ENTIDADE = A.ENTIDADE_CLIENTE
LEFT JOIN NF_FATURAMENTO                    E WITH(NOLOCK) ON E.EMPRESA = A.EMPRESA AND E.NF_FATURAMENTO = A.NF_FATURAMENTO AND E.NF_SERIE = A.NF_SERIE
                                                    AND E.NOTA_DESPACHO = A.NOTA_DESPACHO AND E.ENTIDADE = A.ENTIDADE_CLIENTE
LEFT JOIN NF_FATURAMENTO_CANHOTOS        F WITH(NOLOCK) ON F.NF_FATURAMENTO = E.NF_FATURAMENTO

