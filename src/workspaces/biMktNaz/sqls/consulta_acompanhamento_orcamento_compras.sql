SET NOCOUNT ON

DECLARE @MES     NUMERIC(2)
DECLARE @ANO     NUMERIC(4)

SET @MES     = MONTH('REPLACE_START_DATE')
SET @ANO     = YEAR('REPLACE_START_DATE')


--SET @MES     =3
