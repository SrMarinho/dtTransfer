if object_id('tempdb..#TEMP_RESULTADO') is not null DROP TABLE #TEMP_RESULTADO

SELECT X.MES,
       X.ANO,
       X.COMPRADOR,
       X.GRUPO_COMPRA,
       SUM(X.VALOR_ORCAMENTO)              AS VALOR_ORCAMENTO,
       SUM(X.VALOR_LIMITE_EXTRA)           AS VALOR_LIMITE_EXTRA,
       SUM(X.TOTAL_ORCAMENTO)              AS TOTAL_ORCAMENTO,
       SUM(X.TOTAL_COMPRA_PEDIDOS_ORIGINAL)                AS TOTAL_COMPRA_PEDIDOS_ORIGINAL,
       SUM(X.TOTAL_COMPRAS)                AS TOTAL_COMPRAS,
       SUM(X.TOTAL_ENTRADA_PEDIDOS)        AS TOTAL_ENTRADA_PEDIDOS,
       SUM(X.TOTAL_ENTRADA_OUTROS_PEDIDOS) AS TOTAL_ENTRADA_OUTROS_PEDIDOS,

       CASE WHEN SUM(X.TOTAL_ORCAMENTO) > 0
            THEN ( SUM(X.TOTAL_COMPRA_PEDIDOS_ORIGINAL )  / SUM(X.TOTAL_ORCAMENTO) ) * 100.00
            ELSE CASE WHEN SUM(X.TOTAL_COMPRA_PEDIDOS_ORIGINAL)  > 0
                      THEN -100
                      ELSE 0
                 END
       END                                 AS P_META,

       SUM(X.VALOR_ORCAMENTO) -
       SUM(X.TOTAL_COMPRA_PEDIDOS_ORIGINAL ) AS SALDO_COMPRAS_ORIGINAL,

       SUM(X.TOTAL_ORCAMENTO) -
       SUM(X.TOTAL_COMPRA_PEDIDOS_ORIGINAL ) AS SALDO_COMPRAS,

       SUM(X.TOTAL_ENTRADA_PEDIDOS)        +
       SUM(X.TOTAL_ENTRADA_OUTROS_PEDIDOS) AS TOTAL_ENTRADA_GERAL,

       IDENTITY ( INT , 1 , 1 )                               AS ID


  INTO #TEMP_RESULTADO


  FROM (

SELECT A.MES,
       A.ANO,
       A.COMPRADOR,
       A.GRUPO_COMPRA,
       A.VALOR_ORCAMENTO,
       A.VALOR_LIMITE_EXTRA,
       A.TOTAL_ORCAMENTO,
       0 AS TOTAL_COMPRA_PEDIDOS_ORIGINAL,
       0 AS TOTAL_COMPRAS,
       0 AS TOTAL_ENTRADA_PEDIDOS,
       0 AS TOTAL_ENTRADA_OUTROS_PEDIDOS

  FROM #TEMP_METAS A WITH(NOLOCK)

  UNION ALL

SELECT A.MES,
       A.ANO,
       A.COMPRADOR,
       A.GRUPO_COMPRA,
       0 AS VALOR_ORCAMENTO,
       0 AS VALOR_LIMITE_EXTRA,
       0 AS TOTAL_ORCAMENTO,
       A.TOTAL_COMPRA_PEDIDOS_ORIGINAL ,
       A.TOTAL_COMPRA_PEDIDOS  AS TOTAL_COMPRAS,
       0 AS TOTAL_ENTRADA_PEDIDOS,
       0 AS TOTAL_ENTRADA_OUTROS_PEDIDOS

  FROM #TEMP_COMPRAS A WITH(NOLOCK)


  UNION ALL

SELECT A.MES,
       A.ANO,
       A.COMPRADOR,
       A.GRUPO_COMPRA,
       0 AS VALOR_ORCAMENTO,
       0 AS VALOR_LIMITE_EXTRA,
       0 AS TOTAL_ORCAMENTO,
       0 AS TOTAL_COMPRA_PEDIDOS_ORIGINAL,
       0 AS TOTAL_COMPRAS,
       A.TOTAL_COMPRAS AS TOTAL_ENTRADA_PEDIDOS,
       0 AS TOTAL_ENTRADA_OUTROS_PEDIDOS

  FROM #TEMP_ENTRADAS_PEDIDOS A WITH(NOLOCK)


) X


GROUP BY X.MES,
         X.ANO,
         X.COMPRADOR,
         X.GRUPO_COMPRA

ORDER BY COMPRADOR


SELECT A.ID,
       A.MES,
       A.ANO,
       A.COMPRADOR,
       ISNULL(C.NOME, 'ND') AS COMPRADOR_NOME,
       A.GRUPO_COMPRA,
       B.DESCRICAO          AS GRUPO_COMPRA_DESCRICAO,
       A.VALOR_ORCAMENTO,
       A.VALOR_LIMITE_EXTRA,
       A.TOTAL_ORCAMENTO,
       A.TOTAL_COMPRA_PEDIDOS_ORIGINAL,
       A.TOTAL_COMPRAS,
       A.TOTAL_ENTRADA_PEDIDOS,
       A.TOTAL_ENTRADA_OUTROS_PEDIDOS,
       A.P_META,
       A.SALDO_COMPRAS_ORIGINAL,
       A.SALDO_COMPRAS,
       A.TOTAL_ENTRADA_GERAL

  FROM #TEMP_RESULTADO A WITH(NOLOCK)
  JOIN GRUPOS_COMPRAS  B WITH(NOLOCK) ON B.GRUPO_COMPRA = A.GRUPO_COMPRA
  LEFT JOIN COMPRADORES C WITH(NOLOCK) ON C.COMPRADOR = A.COMPRADOR
  
ORDER BY ID

