SET NOCOUNT ON
DECLARE @DATA_INICIAL DATE = IIF('REPLACE_START_DATE' = '  /  /    ', NULL, 'REPLACE_START_DATE');
DECLARE @DATA_FINAL   DATE = IIF('REPLACE_END_DATE' = '  /  /    ', NULL, 'REPLACE_END_DATE');

IF OBJECT_ID('TEMPDB..#TEMP_NF') IS NOT NULL DROP TABLE #TEMP_NF;
SELECT A.FORMULARIO_ORIGEM,
       A.TAB_MASTER_ORIGEM,
       A.NF_FATURAMENTO AS REG_MASTER_ORIGEM,
       A.NF_FATURAMENTO AS REGISTRO_PROCIFT,
       A.NF_NUMERO,
       A.EMPRESA,
       A.EMISSAO,
       A.OPERACAO_FISCAL,
       A.DATA_HORA,
       A.CHAVE_NFE,
       A.USUARIO_LOGADO AS USUARIO,
       E.NOME AS USUARIO_NOME,
       CASE WHEN D.STATUS = 2 THEN 'EM FILA P/ PROCESSAMENTO'
            WHEN D.STATUS = 3 THEN 'COM INCONSCISTENCIA'
            WHEN D.STATUS = 4 THEN 'AUTORIZADA'
            ELSE ISNULL(CAST(D.STATUS AS VARCHAR), 'SEM STATUS')
       END AS STATUS,
       G.PRODUTO,
       G.DESCRICAO AS DESCRICAO_PRODUTO,
       F.QUANTIDADE,
       F.TOTAL_PRODUTO,
       SUM(C.TOTAL_PRODUTOS) AS TOTAL_PRODUTOS,
       SUM(C.TOTAL_GERAL)    AS TOTAL_GERAL,
	   F.VALIDADE
    INTO #TEMP_NF
    FROM NF_FATURAMENTO          A WITH(NOLOCK)
    JOIN NF_FATURAMENTO_PRODUTOS F WITH(NOLOCK) ON F.NF_FATURAMENTO    = A.NF_FATURAMENTO
    JOIN PRODUTOS                G WITH(NOLOCK) ON G.PRODUTO           = F.PRODUTO
    JOIN PP_OPS_DETALHES         B WITH(NOLOCK) ON B.OPERACAO_FISCAL   = A.OPERACAO_FISCAL
                                               AND B.EMPRESA           = A.EMPRESA
    JOIN NF_FATURAMENTO_TOTAIS   C WITH(NOLOCK) ON C.NF_FATURAMENTO    = A.NF_FATURAMENTO
    JOIN USUARIOS                E WITH(NOLOCK) ON E.USUARIO           = A.USUARIO_LOGADO
    LEFT
    JOIN NFE_CABECALHO           D WITH(NOLOCK) ON D.FORMULARIO_ORIGEM = A.FORMULARIO_ORIGEM
                                               AND D.TAB_MASTER_ORIGEM = A.TAB_MASTER_ORIGEM
                                               AND D.REG_MASTER_ORIGEM = A.REG_MASTER_ORIGEM
        WHERE A.EMISSAO BETWEEN @DATA_INICIAL AND @DATA_FINAL
        GROUP BY A.FORMULARIO_ORIGEM, A.TAB_MASTER_ORIGEM, A.REG_MASTER_ORIGEM, A.NF_FATURAMENTO, A.EMPRESA, A.EMISSAO, A.OPERACAO_FISCAL, A.DATA_HORA, A.EMISSAO, A.CHAVE_NFE, A.USUARIO_LOGADO, D.STATUS, E.NOME, A.NF_NUMERO, G.PRODUTO, G.DESCRICAO, F.QUANTIDADE, F.TOTAL_PRODUTO, F.VALIDADE

