SELECT A.FORMULARIO_ORIGEM,
       A.TAB_MASTER_ORIGEM,
       A.NF_COMPRA AS REG_MASTER_ORIGEM,
       A.NF_COMPRA AS REGISTRO_PROCIFT,
       A.NF_NUMERO,
       A.EMPRESA,
       A.MOVIMENTO AS EMISSAO,
       A.OPERACAO_FISCAL,
       A.DATA_HORA,
       ISNULL(A.CHAVE_NFE, D.CHAVE_NFE) AS CHAVE_NFE,
       A.USUARIO_LOGADO AS USUARIO,
       E.NOME AS USUARIO_NOME,
       CASE WHEN D.STATUS = 2 THEN 'EM FILA P/ PROCESSAMENTO'
            WHEN D.STATUS = 3 THEN 'COM INCONSCISTENCIA'
            WHEN D.STATUS = 4 THEN 'AUTORIZADA'
            ELSE ISNULL(CAST(D.STATUS AS VARCHAR), 'SEM STATUS')
       END AS STATUS,
       G.PRODUTO,
       G.DESCRICAO AS DESCRICAO_PRODUTO,
       F.QUANTIDADE_ESTOQUE AS QUANTIDADE,
       F.TOTAL_PRODUTO,
       SUM(C.TOTAL_PRODUTOS) AS TOTAL_PRODUTOS,
       SUM(C.TOTAL_GERAL)    AS TOTAL_GERAL,
	   F.VALIDADE
    FROM NF_COMPRA             A WITH(NOLOCK)
    JOIN NF_COMPRA_PRODUTOS    F WITH(NOLOCK) ON F.NF_COMPRA       = A.NF_COMPRA
    JOIN PRODUTOS              G WITH(NOLOCK) ON G.PRODUTO         = F.PRODUTO
    JOIN PP_OPS_DETALHES       B WITH(NOLOCK) ON B.OPERACAO_FISCAL = A.OPERACAO_FISCAL
                                             AND B.EMPRESA         = A.EMPRESA
    JOIN NF_COMPRA_TOTAIS      C WITH(NOLOCK) ON C.NF_COMPRA       = A.NF_COMPRA
    JOIN USUARIOS              E WITH(NOLOCK) ON A.USUARIO_LOGADO  = E.USUARIO
    LEFT
    JOIN NFE_CABECALHO         D WITH(NOLOCK) ON D.FORMULARIO_ORIGEM = A.FORMULARIO_ORIGEM
                                             AND D.TAB_MASTER_ORIGEM = A.TAB_MASTER_ORIGEM
                                             AND D.REG_MASTER_ORIGEM = A.REG_MASTER_ORIGEM
        WHERE A.MOVIMENTO BETWEEN @DATA_INICIAL AND @DATA_FINAL
          AND A.STATUS_PROCESSAMENTO_INTEGRACAO = 1
        GROUP BY A.FORMULARIO_ORIGEM, A.TAB_MASTER_ORIGEM, A.NF_COMPRA, A.EMPRESA, A.EMISSAO, A.OPERACAO_FISCAL, A.DATA_HORA, A.CHAVE_NFE, A.USUARIO_LOGADO, E.NOME, D.STATUS, D.CHAVE_NFE, A.MOVIMENTO, A.NF_NUMERO, G.PRODUTO, G.DESCRICAO, F.QUANTIDADE_ESTOQUE, F.TOTAL_PRODUTO, F.VALIDADE

