        A.FORMULARIO_ORIGEM AS formulario_origem,
        A.TAB_MASTER_ORIGEM AS tab_master_origem,
        A.REG_MASTER_ORIGEM AS reg_master_origem,
        A.REGISTRO_PROCIFT AS registro_procift,
        A.NF_NUMERO AS nf_numero,
        A.EMPRESA AS empresa,
        A.EMISSAO AS emissao,
        A.OPERACAO_FISCAL AS operacao_fiscal,
        A.DATA_HORA AS data_hora,
        A.CHAVE_NFE AS chave_nfe,
        A.USUARIO AS usuario,
        A.USUARIO_NOME AS usuario_nome,
        A.STATUS AS status,
        A.PRODUTO AS produto,
        A.DESCRICAO_PRODUTO AS descricao_produto,
        A.QUANTIDADE AS quantidade,
        A.TOTAL_PRODUTO AS total_produto,
        A.TOTAL_PRODUTOS AS total_produtos,
        A.TOTAL_GERAL AS total_geral,
        B.DESCRICAO AS formulario,
        CAST(A.VALIDADE AS DATE) AS validade
FROM
        #TEMP_NF    A
        JOIN FORMULARIOS B ON B.NUMID = A.FORMULARIO_ORIGEM
WHERE NOT EXISTS(SELECT TOP 1 1 FROM NFE_CANCELAMENTOS ZZ WHERE ZZ.IDNOTA = A.CHAVE_NFE)